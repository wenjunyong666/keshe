/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
  static uint8_t cnt_10ms=0;
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  cnt_10ms++;
  if(cnt_10ms==5)
  {
    if(nrf2401_tx_flag==2)
    {
      RX2TX();
      NRF24L01_TxPacket((uint8_t*)&nrf2401_buf,buf_pos); // send queued NRF packet
      TX2RX();
      if(g_bridge_repeat_left > 1U)
      {
        g_bridge_repeat_left--;
      }
      else
      {
        g_bridge_repeat_left=0U;
        nrf2401_tx_flag=0;
        buf_pos=0;
      }
    }
  }
  else if(cnt_10ms>=10)
  { SysTick_count++;
    RC_Analy();
    if(((SysTick_count%100)==0) && (g_remote_soft_sleeping == 0U))
    { HAL_GPIO_TogglePin(LED1_GPIO_Port,LED1_Pin);
      HAL_GPIO_TogglePin(LED2_GPIO_Port,LED2_Pin);
    } 
    cnt_10ms=0;
  }
  
  /* USER CODE END SysTick_IRQn 1 */
}

